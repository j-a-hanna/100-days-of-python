# Day 02: Practice with variables

#1. Create variables.
name = "Jeanette"
age = 28
is_learning_python = True

#2. Print variable values.
print("Name:", name)
print("Age:", age)
print("Learning Python:", is_learning_python)

#3. Update a variable.
age = age + 1  # Increment age by 1
print("Next year, I will be", age)

#4. Use input to make it interactive.
user_name = input("What is your name? ")
print("Nice to meet you,", user_name)

#5. Bonus: Combine variables in a sentence.
favorite_language = "Python"
message = f"{user_name} is learning {favorite_language} and loving it!"
print(message)